// variables are used to store data in memory and we can use them later in our code
// we can declare a variable using the var, let, or const keywords

// var is function-scoped and can be redeclared and updated
var name = "Elbasry";
console.log(name); // Output: Elbasry

// let is block-scoped and can be updated but not redeclared
let age = 20;
console.log(age); // Output: 20

// const is block-scoped and cannot be updated or redeclared
const country = "Sudan ";
console.log(country); // Output: Sudan


