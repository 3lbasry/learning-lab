// Object declaration
let student = {
  name: "Elbasry",
  age: 20,
  country: "Sudan",
};

console.log(student.name); // Output: Elbasry
console.log(student.age); // Output: 20
console.log(student.country); // Output: Sudan
console.log(student); // Output: { name: 'Elbasry', age: 20, country: 'Sudan' }

// Array declaration
let student2 = [20, "Elbasry", "Sudan"];
console.log(student2[0]); // Output: 20
console.log(student2[1]); // Output: Elbasry
console.log(student2[2]); // Output: Sudan
console.log(student2); // Output: [ 20, 'Elbasry', 'Sudan' ]


